// statements
// if .. else
let a = 11; // 10 , 11 ,12
if (a === 10) {
  // true
  console.log(" a is equals to 10");
} else if (a === 11) {
  // false
  console.log(" a is equals to 11");
} else if (a === 12) {
  console.log(" a is equals to 12");
} else {
  console.log(" a is not equals to 10,11,12");
}
// switch
switch (a) {
  case 10 || 11:
    console.log(" a is equals to 10 or 11");
    break;
  case 12:
    console.log(" a is equals to 12");
    break;
  default:
    console.log(" a is not equals to 10,11,12");
    break;
}
